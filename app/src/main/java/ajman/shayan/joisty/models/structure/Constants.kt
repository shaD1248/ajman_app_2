package ajman.shayan.joisty.models.structure

const val kgf = 1.0
const val m = 100.0 // cm
const val s = 1.0

const val m2 = m * m
const val m3 = m * m * m
const val cm = 0.01 * m
const val cm2 = cm * cm
const val s2 = s * s

const val g = 9.807 * m / s2
const val N = kgf * m / s2 / g

const val MPa = 1000000 * N / m2
const val Es = 200000 * MPa
const val S235JR = 235 * MPa
const val C20 = 20 * MPa

const val gamma_b = 14 * kgf / m3
const val gamma_c = 2400 * kgf / m3
const val gamma_rc = 2500 * kgf / m3
const val gamma_s = 7850 * kgf / m3